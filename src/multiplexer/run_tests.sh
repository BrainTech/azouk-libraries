#!/bin/bash

for i in `find build -name 'test_*' | grep -vE '~$'`; do
    echo
    echo "---------------------------------------------------------------------------------------------------------------------"
    echo "Running $i"
    echo "---------------------------------------------------------------------------------------------------------------------"
    "$i"
    declare -i ext=$?
    if [ $ext -ne 0 ]; then
	echo "FAILED: $ext"
	exit $ext
    fi
done
